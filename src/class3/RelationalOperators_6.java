package class3;

public class RelationalOperators_6 {
    public static void main(String[] args) {
        // Arithmetic operators give us the same data type
        int number=10;
        int number2=10;
        System.out.println(number+number2);

        // Relational operators always return boolean value

        /*
        == equal to
        != not equal to
        > greater than
        < less than
        >= greater or equal
        <= less or equal
         */
        boolean result=20>10;

        System.out.println(result);
        System.out.println(5>10);
        System.out.println(5!=10);
        System.out.println(5==10);
        System.out.println(5>=10);
        System.out.println(5<=10);
        System.out.println(5<=5);





    }
}
